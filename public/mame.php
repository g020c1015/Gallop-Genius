  <div>
    <h2>オッズとは</h2>
    <p>オッズとは簡潔にいうと馬券が的中した場合どれくらいの配当がつくのかを倍率で示したものです。</p>
    <p>例えば、オッズが5.0倍の馬券を1000円分購入し、的中した時の配当は1000円×5.0で5000円の払い戻しとなります。</p>
    <p>このオッズの数値はどれくらいその馬券が購入されているかで数値が変わります。</p>
    <p>よって買われれば買われるほどオッズが下がります。</p>
    <p>オッズは発売している間は常に変動し続けるので、買ったときは10.0倍だったのに締めきったら6.0倍しか無いことなどもよくあることです。</p>
  </div>
  <div>
    <h2>牡馬、牝馬、セン馬とは</h2>
    <p>出馬表によく牡、牝、セなどの文字があると思います。</p>
    <p>これらは馬の性別になります。</p>
    <p>牡馬がオス馬</p>
    <p>牝馬がメス馬</p>
    <p>セン馬はもともと気性が荒い馬などが落ち着かせるために去勢をした馬となります。</p>
  </div>
  <div>
    <h2>斤量とは</h2>
    <p>斤量とはレースの施行条件によって競走馬がレースで負担する重量になります。</p>
    <p>斤量=ジョッキーの体重+馬具（衣服、プロテクター、ヘルメット）になります。</p>
    <p>ジョッキーはレースごとに斤量が変わるのでその日のレースの最低斤量に合わせるので、毎週の体重管理が非常に重要になってしまいます。</p>
  </div>
  <div>
    <h2>レース施行条件について</h2>
    <p>施行条件は３つに分けることができます。</p>
    <ul>
      <li>定量戦・馬齢戦</li>
      <li>別定戦</li>
      <li>ハンデ戦</li>
    </ul>
    <p>定量戦・馬齢戦は獲得賞金に関係なく、馬の年齢や性別により、出走馬全馬に、一定の負担重量を定める方法になります。G1レースなどがこの方法が多いです。</p>
    <p>別定戦とは獲得しているタイトルや賞金によって負担重量を定める方法になります。G2競争などで多く見られます。</p>
    <p>ハンデ戦とはJRAのハンデキャッパーという人が過去の実績や近走内容からゴール前で横一列になるように負担重量を定める方法になります。</p>
    <p>ハンデ戦は弱い馬でも強い馬に勝てる可能性もあるので、比較的配当も荒れやすいと言われています。</p>
